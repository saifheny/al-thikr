export async function fetchSurahList() {
  const res = await fetch('https://api.alquran.cloud/v1/surah');
  if (!res.ok) throw new Error('Failed to fetch Surah list');
  const result = await res.json();
  return result.data;
}

export async function fetchSurahDetail(surahNum, reciterId) {
  const [arabicRes, translationRes] = await Promise.all([
    fetch(`https://api.alquran.cloud/v1/surah/${surahNum}/${reciterId}`),
    fetch(`https://api.alquran.cloud/v1/surah/${surahNum}/en.sahih`)
  ]);
  if (!arabicRes.ok) throw new Error('Failed to fetch Arabic recitation');
  if (!translationRes.ok) throw new Error('Failed to fetch translation');
  const arabicData = await arabicRes.json();
  const translationData = await translationRes.json();
  const surah = arabicData.data;
  const translationAyahs = translationData.data.ayahs;
  surah.ayahs = surah.ayahs.map((ayah, index) => ({
    ...ayah,
    translation: translationAyahs[index] ? translationAyahs[index].text : ''
  }));
  return surah;
}

export async function fetchAyahTafsir(ayahNumber) {
  const res = await fetch(`https://api.alquran.cloud/v1/ayah/${ayahNumber}/ar.muyassar`);
  if (!res.ok) throw new Error('Failed to fetch Tafsir');
  const result = await res.json();
  return result.data.text;
}

/* ── Offline Caching Service (Cache API) ── */
const AUDIO_CACHE_NAME = 'quran-audio-offline-cache';

export async function checkSurahCached(ayahs) {
  if (!ayahs || ayahs.length === 0) return false;
  try {
    const cache = await caches.open(AUDIO_CACHE_NAME);
    const audioAyahs = ayahs.filter(a => a.audio);
    if (audioAyahs.length === 0) return false;
    for (const ayah of audioAyahs) {
      const match = await cache.match(ayah.audio);
      if (!match) return false;
    }
    return true;
  } catch (e) {
    return false;
  }
}

export async function getCachedAudioUrl(audioUrl) {
  if (!audioUrl) return '';
  try {
    const cache = await caches.open(AUDIO_CACHE_NAME);
    const cachedResponse = await cache.match(audioUrl);
    if (cachedResponse) {
      const blob = await cachedResponse.blob();
      return URL.createObjectURL(blob);
    }
  } catch (e) {
    console.error("Cache read error:", e);
  }
  return audioUrl;
}

export async function cacheSurahAudio(ayahs, onProgress) {
  try {
    const cache = await caches.open(AUDIO_CACHE_NAME);
    const audioAyahs = ayahs.filter(a => a.audio);
    const total = audioAyahs.length;
    if (total === 0) return true;

    let cachedCount = 0;
    for (let i = 0; i < audioAyahs.length; i++) {
      const ayah = audioAyahs[i];
      try {
        const match = await cache.match(ayah.audio);
        if (!match) {
          const res = await fetch(ayah.audio);
          if (res.ok) {
            await cache.put(ayah.audio, res.clone());
          } else {
            throw new Error(`Failed to fetch ${ayah.audio}`);
          }
        }
        cachedCount++;
        if (onProgress) {
          onProgress(cachedCount, total);
        }
      } catch (err) {
        console.error(`Failed to cache ayah ${ayah.number}:`, err);
      }
    }
    return true;
  } catch (e) {
    console.error("Cache open/put error:", e);
    throw e;
  }
}
